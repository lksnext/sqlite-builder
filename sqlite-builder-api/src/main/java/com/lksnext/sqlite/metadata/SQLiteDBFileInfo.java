package com.lksnext.sqlite.metadata;

public class SQLiteDBFileInfo {

	private String file;
	private String md5;

	public String getFile() {
		return file;
	}

	public void setFile(String file) {
		this.file = file;
	}

	public String getMd5() {
		return md5;
	}

	public void setMd5(String md5) {
		this.md5 = md5;
	}

}
